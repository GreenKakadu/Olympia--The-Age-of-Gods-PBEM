New players often struggle over how to make money in The Age of Gods.  There are many opportunities; here are a few to get you started.

<p/>
<h3>Markets</h3>
<p/>
<ul> 
<p>Each city buys a quantity of baskets [95] or clay pots [94].  (These can be made out of no material by any noble with the MAKE command.)  Port cities buy fish.  (You need a boat and sailors to gather fish.)</p>
<p>Small cities buy a certain quantity of wood [77], stone [78], and wild horses [51] each turn.   (You have to collect these with an appropriate skill.)</p>

<p>Large cities buy lana bark [64], avinia leaf [65], spiny root [66], farrenstone [67], yew [68], mallorn wood [70] and pretus bones [71].  (Again, you must collect these with an appropriate skill.)</p>

<p>When dealing with markets, remember that the highest bidder and the lowest seller win.  If a market is buying baskets for 3 gold, and you offer to sell at 2 gold, you'll sell your baskets before someone asking for 3 gold. </p>

<p>The Increase Demand and Increase Supply skills can increase your profits when buying and selling at a market (e.g. the city could buy 150 fish at 4 gold each turn, rather than 100 fish at 2 gold each).</p>
</ul>
<h3>Trading</h3>
<p/>
<ul>
<p>Trading is buying a good in one market and selling it in another.  Markets are located in cities and in trading guilds.</p>
<p>There are a few items that can be bought in city markets and sold at other cities without having an experienced trader: </p>
 
<ul>
<li>fish oil [97]</li>
<li>dried fish [255]</li>
<li>oxen [76]  (can be made into leather [80] with the command MAKE 80.</li>
</ul>
<p>There are many items that can be bought and sold in the trading guilds.  Find a trading guild selling an item and another buying it, and set up a trade route.  Trading between guilds can be extremely profitable, especially when augmented by trading skills.</p> 
</ul> 

<p/>
<h3>Castles</h3>
<p/>
<ul> 
 
<p>When you build a castle and garrison populated provinces to it, you can tax the peasants in the province, as well as the markets in the cities and trade guilds.  This can yield as much as 1000 gold per garrison per month.</p>
</ul> 

<p/>
<h3>Monster Hunting </h3>
<p/>
<ul> 
 
<p>Most monsters have some sort of treasure (gold, magic items, trade goods, etc.)  Killing monsters also gives you "monster parts" such as wolf hides.    Cities pay a small bounty for monster parts,  but you have to find the right city to claim your bounty.</p>
</ul> 

<p/>
<h3>Inns</h3>
<p/>
<ul> 

 
<p>Inns generate about 50-100 gold per month, but require that your noble be the top noble in the inn at the end of each month.  You should only build one inn in each province.</p>
</ul> 
<p/>
<h3>Magicians and Priests</h3>
<p/>
<ul> 

 
<p>Mages with "Perform common tasks for gold" can make up to 91 gold per week with practice.</p>
<p>Priests who own a temple make about 100 gold at the end of each month.</p>
</ul> 

<p/>
<h3>Mining</h3>
<p/>
<ul> 

 
<p>You can mine for gold, but iron is a more precious commodity, as paladins, heavy crossbows, heavy wagons, and the like all require lots of iron to make.</p>
</ul> 
<p/>
<h3>Miscellaneous</h3>
<p/>
<ul> 
 
<p>Nobles with the Stealth skill "Petty Thievery" can make up to 70 gold per week by stealing stuff from peasants.</p>
<p>If you find a poppy field, you can get the locals addicted to opium (dealing in opium can make you unpopular with other players, as it adversely affects the peasant population)</p>
<p>Of course, you can always negotiate to do tasks for other players to make money!  Some of them are likely very rich, and need some task done that they will hire out.</p>
</ul> 
<p/>
<h3>Competition and Consequences</h3>
<p/>

<ul> 
<p>You don't have to get shut out of a market.  Buy for more or sell for less!</p>
<p>Manipulating the markets, undercutting the competition, and so on will likely affect other players.  Take that into account.  For example, it's polite to ask permission from a city's ruler before you manipulate the city markets.</p>
</ul> 

