<h2>Some quick hints for a good start</h2>
<p/>
<p/>
<b><i>Taken from a TAG Neverending Kingdom alliance web site.  Author unknown</i></b>
<p/>
<ul>
<li>Be certain to read the rules at least once, but skim through them often.
You begin with 12 Noble Points (NP). These are the most valuable item in the game and therefore must be spent wisely.  NP can be used to:
  <ul>
  <li>form new nobles</li>
  <li>help your nobles learn magic and religion skills</li>
  <li>"oath" your nobles so you do not have to pay them each month (and increase their loyalty)</li>
  </ul>
</li>
<li>In the first turn, try to form 3-4 nobles.  With more nobles you can do more in your turn, but they cost 1 NP each to form.</li>
<li>5000 gold may seem a lot of money at the start, but it is not! Try not to spend it learning all the skills and buying all the books that are in the market. It is better contact other player and try to exchange knowledge than buy books by yourself.  Learn the skills that will provide your faction with a source of income.</li>
<li>It almost never makes sense to buy wood, from a city or another noble. Forestry is one of the basic skills, and once learned, wood is free. Before you begin harvesting resources, (wood, stone, peasants, etc.) check to see what restrictions, if any, have been placed on your area by the controlling alliance/faction.</li>
<li>Since only the first week your noble spends studying in a turn receives the full learning benefit (7 days of study=7 days of learning), be careful using books and study points. Use them only in first week your noble studies during the month as to not spoil the contents of the book or waste study points.</li>
<li>Don't build your free tower in the first place you arrive, but keep it for the place you will establish your faction.  Usually there are some public towers in every city that you can use. Is recommended, though, to ask permission before just entering an occupied tower.</li>
<li>Definitely DO NOT order a noble to walk or ride alone (with no other nobles stacked with him) outside of cities. The wilderness IS very dangerous. Players used to former versions of Olympia tend to forget this rule and usually loose their nobles to monster attacks!  Garrisoned provinces provide some measure of safety, but monsters occasionally enter them or attack the garrison.</li>
<li>Do not build a second inn in a location where one has already been built. This will divide the income and the owner of the other inn won't be very happy.</li>
<li>Communication is the key to success in a game environment like this. So keep in touch with your friends and enemies alike. The contact with other players is the most enjoyable aspect of the game. Playing "the lonely guy" deprives you of the most fun.</li>
<li>You can contact other nobles' owners through the game interface if you don't know who they belong to by sending email via the diplomatic missive instructions.</li>
<li>If you are willing to work together with other players try to join an alliance, but before joining check the alliance goals and the rights and duties you will have.</li>
<li>Try not to concentrate only in a specific skill (like combat per example) but have instead a variety of skills known. This will make your faction more versatile and fun to play with.</li>
<li>Find a place to call home. It is not necessary that it is a castle. One or more buildings controlled by you can provide the means to upkeep your whole faction. Also a single noble with trading skills can help a lot. Most alliances have restriction about trading in their territory but communication can grant you access to the trading guilds where the real valuable goods are.</li>
<li>Don't wait until the last moment to send your orders, but prefer to send them right after you receive your report. You can change them later, and in case of some last minute problems with your connection or any other kind of unforeseen circumstances you won't waste the turn.</li>




