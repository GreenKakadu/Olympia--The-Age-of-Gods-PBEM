<b><i>
An intro to TAG for players migrating from the G2-based source, by Lord Corum of
Tanelorn. Most of this is still applicable, although it was written in about 2000.
Edited and updated in 2011 by Valen with permission.</i></b><br>
<p/>
<h1>TAG Primer for Olympia G2 players.</h1>
<p/>

<p>
First off, if you have played Olympia G2 then you already know the
most important fact of learning TAG. READ THE RULES! READ THEM
AGAIN!! If you are like me and play both, READ THEM REGULARLY!!!
</p>

<p>As you can tell there are some major diffrences between Oly G2 and TAG.
Most of the diffrences are very obvious but can still cause you
problems.
</p>

<p>First off and probably most important is when you join TAG. Check 
the details of your game and read some issues of The Gods Speak (TAG
equivalent of the Times) to determine where to start.  In some cases,
it may be predetermined for you, but in the event you can choose, you
probably want to start in an area with friendly factions and not a war
zone.</p>
<p>In open-ended games, you will want to start someplace where other
players are available to teach you the basic skills.  Check the player
list or ask on the forum to get some pointers on the best places to
start.</p>

<p/>
<h3>Noble Points: </h3>
<p/>
<p>Unlike Oly G2 if a noble dies or is lost for some reason, you do NOT
get the noble points back. There is no NP recovery in this game.  </p>
<p/>
<h3>Wilderness: </h3>
<p/>
<p>In Oly G2 wilderness only means that you collect very little in taxes.
In TAG the wilderness is dangerous. There are wandering monsters and
bandits that prey in the non-civilized provinces. These random dangers 
can and do kill and capture nobles.
The best way to prevent that is to not wander out alone, always take
troops with you when you leave a city.
</p>
<p/>
<h3>Learning Skills: </h3>
<p/>
<p>Very large diffrence here. To date about 10% of cities found teach a skill.
The best way to learn a skill in TAG is to have another player teach you
that skill. Currently the best source for skill instruction in TAG is
other players.
</p>
<p>Also, you cannot study a catagory skill for 1 day then go elsewhere and
finish that study without instruction. You must have instruction for
all the days (mostly 7) required to learn a catagory skill. Once you have
learned the catagory skill you can study most sub-skills anywhere at
any time (the exception to this being Guild Skills (see below))
</p>
<p>TAG has Study Points, these are very diffrent from Oly G2's Fast
Study Days. Study Points allow you to study a basic catagory skill
(Combat, Shipcraft, Construction, Forestry, Mining, Ranger, or Trading)
without a source of instruction. You start with enough study points
to learn 2 or 3 Catagory Skills so use them wisely.
</p>
<p>There is a penalty for too broad of learning in a Noble. It will cost
you a Noble Point for catagory skill beyond the third learned. To
counterbalance this there is also a "forget" command that allows you to
forget a skill. This can get very costly with a skill that already costs
a Noble Point to learn. For instance, there are 5 magic skills, each one past
the third you learn (assuming you have either not learned or forgotten any other
non-magic skills) will cost you 2NP instead of 1.
</p>
<p>You no longer have to reasearch skills. There are three types of
sub-skills under each
catagory skill, teachable, unteachable, and guild skills. Teachable
skills are exactly that,
they can be taught to you by another noble using the "teach" command,
this allows you to learn the skill at an accelrated rate (2X or 4X).
Unteachable skills are skills that cannot
be taught, all that means is that you cannot learn them at an
accelerated rate. See below
for Guild Skills.
</p>
<p>The final major diffrence in learning is Guilds. Every catagory
skill has some sub-skills that are only available to Guild members.
To become a Guild member you must know ALL of the non-guild skills,
then you enter or form a guild hall and become a member of that guild.
You must be inside a Guild Hall to study a Guild skill, and you must be
in the guild hall
for the entire time you spend studying the skill, you cannot begin
study then leave
the guild hall and study elsewhere (except another guild hall). A noble
can
only be a member of one guild, and once he joins a guild he/she/it is
forever bound to that guild.
</p>
<p/>
<h3>Combat </h3>
<p/>
<p>The combat mechanics in TAG are little diffrent that Oly G2. There
are, however, some major diffrences in training and controling men
in battle.
</p>
<p>The first, and most obvious, is that there are more types of combat
troops in TAG. Most combat troops require a diffrent subskill of combat
to train.
</p>
<p>The second, and most troublesome for Oly2 vetrans, is that one noble
is limited in the number of troops he/she/it can control. The days
of the untrained noble controling hundreds of troops in battle are
over.
A noble starts with the ability to control 10 men when attacking and
30 men when defending. There is a sub-skill under COMBAT called
Control Men In Battle, it allows you to control more men in combat.
However, it costs 3 gold and 3 or more days for every additional man
you can control in combat.
</p>
<p>Use Beasts in Battle has been moved to the Combat catagory.
</p>
<p>SFW, Defense, Swordplay, and Archery are now under other catagory
skills.
</p>
<p>There is a new Combat related Catagory Skill. It is called Heroism.
Heroism is concerned with all of the skills required to turn your noble
into a one-man/woman/it army. This is where Swordplay, defense, and SFW
have been moved to. In addition there are several other skills here
that can increase the effectiveness of a noble in combat. Most skills
in this catagory are only useful when the noble is fighting alone
(although
he/she/it may be stacked with other nobles.)
</p>
<p>There are now combat spells in all magic catagories.
</p>
<p>For each missile troop in the rear there must be a melee troop in
front.
</p>
<p>You no longer go directly from peasants to soilders, there is now an
in-between step called a skirmisher [36]. You must also have a
sub-skill
of combat called Instill Combat Discipline to train soilders.
</p>
<p>Troops can "break". This has to do with non-combat men and
skirmishers, but when one of them dies there is a chance that all of
them will run away. the more that die in combat the better chance that
the rest will run away, leaving your noble standing there alone.
</p>
<p>Every terrain except for Plains can adjust the defensive rating of
the defender.
These defenisve bonuses (in swamps it is a penalty) only apply to units
in the outer province or in cities but not to other sub-locations.
</p>
<p>An interesting sub-note to combat here. In TAG you MUST close the
borders of a castle with a specific order. If you do not issue the
BORDER xxx CLOSE command for your castle any noble not specifically on
your HOSTILE list can walk right
into your castle, thereby attacking your army inside its own walls and
eliminating
the benifits that come from fighting inside a castle. You can also make
your castle
impervious to missile troops by building a moat around your castle.
</p>
<p/>
<h3>Religion.  </h3>
<p/>
<p>Religion has been greatly altered in TAG. Instead of being a single
catagory skill there are nine catagory skills dealing with religion.
There is a base religion catagory skill that is automatically learned
when you learn one of the other religion skills, this is almost
identical
to the Religion skill in G2. The reason for most of the diffrences lies
in one of the fundamental diffrences between G2 and TAG, Mage/Priest
Antipathy. Most TAG games are closed-ended (you can win them) in these
there are three nations, The Imperial Empire cannot use magic, the
Mandor Confederacy cannot use religion skills, The Pen can use neither.
because of this split, Religion has been greatly expanded into eight
Churches, each of which has a speaciality.
</p>
<p>Nobles can only be raised by priests of the church they are
"dedicated" to.
</p>
<p>Mages cannot be deditcated to a priest (makes necro cruicial for
mages).
</p>
<p>Religious abilities require "Piety" (simaler to Aura in use but very
diffrent
in other ways).
</p>
<p>A priest is limited to one church (school of religion).
</p>
<p/>
<h3>Magic </h3>
<p/>
<p>Very little has changed in magic. The largest change to magic is the
addition of combat spells. The loss of an easy SFW and removal of a
heal spell
from magic have increased the importance of Alchemists (as the Religion
heal
cannot be used on a mage).
</p>
<p>A recent change has been made to Appear Common (the magic skill that
surpesses the MAGE
tag in a charachter description). It is no longer an automatic skill.
The mage must
actually cast the spell expending one point of aura for each turn he
wishes it concealed.
</p>
<p>A very impotant note to those who play G2. Weather Magic is now a
priest skill and
is not available to mages.
</p>
<p/>
<h3>Sailing </h3>
<p/>
<p>It is unwise to set out beyond coastal waters without having a ship
captain who is a Shipcraft Guild Member. Sailing "beyond the sight
of land", which means not being adjacent to a land province, can
subject you to storms or being lost at sea (literelly sailing a
diffrent
direction than you intended). There is a Guild sub-skill of shipcraft
called Navigate Deep Seas that allows you to avoid these dangers.
</p>
<p/>
<h3>Beastmastry </h3>
<p/>
<p>Beastmastry has ceased to exist as a catagory skill in TAG. The
summary below was written by Doug Christensen:
</p>
<p><cite>
The G2 Beastmastery skillset has been divided amongst three category
skills: Combat, Ranger, and Church of Dol.
</cite></p>
<p>All an army commander needs to learn is 1195: "Use beasts in
battle", a Combat guild skill.
</p>
<p>The Ranger category inherited the horsemanship skills. Catching and
training horses are Ranger unteachable skills.
</p>
<p>Finally, the Church of Dol governs the detecting, catching and
breeding of wild beasts. Upon re-reading the rules on the Church of Dol
for the fifth time, I finally understood that <b>wild beasts cannot be
captured in combat</b>. (this doesn't apply to horses held by wild
beasts). A priest of Dol wanders the countryside searching for wild
beasts, aided by Detect Beasts. Upon stumbling upon some, he
prays that Dol will charm some of them to follow him. No combat is
involved. When fighting a stack of wild beasts or a stack in which
a noble possesses wild beasts, the beasts in the losing stack are
always lost; <b>they are never placed in the victor's inventory</b>.
</p>
<p/>

<p/>
<h3>Economy</h3>
<p/>
<p/>
TAG has a deeper level of complexity than G2/G3 in it's economic system that encourages player interaction within a kingdom. That is, it is to the benefit of the "king" to have a bunch of merchants (who aren't necessarily part of their alliance) buying and selling goods in the city markers within their kingdom. The biggest differences are the addition of tax controls and supply/demand in the markets.

<ul>
Taxes:
  <ul>
  <li>G3: garrisons collect a tax from the population, based on civ level, and forward some to the castle owner automatically. Pretty straight-forward.</li>
  <li>TAG: there are three types of taxes in TAG, and all may be set by the castle owner with the "tax" command. Taxes are collected and held in the local garrison, and must be collected by a noble rather than automatically being forwarded to the castle owner. The castle owner may set:</li>
    <ul>
    <li>market tax -- 0% to 25% -- collected on goods sold in the city market or trade guild</li>
    <li>province tax -- a 0% to 10% tax on the peasantry. Lower taxes = higher population growth</li>
    <li>city entrance fee -- tax on stacks entering the city, either by weight or by number of men.</li>
    </ul>
  </ul>
Trade:
  <ul>
  <li>G3: A trader founds a trade route which can then be run continuously each turn by any noble or controlled monster stack. Prices are fixed for trade goods as well as for regular goods such as oxen, iron, and fish. Once founded, the trade route is a low-maintenance cash cow.</li>
  <li>TAG: A trader not only founds the routes, but requires continuous interaction to manipulate the markets (supply and demand) and smuggle goods (avoiding taxes). They have exclusive access to the city Trade Guild, where all of the high-value items are sold. </li>
    <ul>
    <li>Supply / Demand is in effect -- driving up the "sell" price of items that are in high demand, and lowering the price of those items that are not. So, a ship's captain who finds a port city that buys fish, but where no one has sold fish recently, will get a much better price for her load than if she sold in the same city each month.</li>
    <li>Demand for "Bounty items" -- these are animal parts that cities purchase from monster-hunting stacks. So one can not only collect the monster's treasure, but also collect a bounty in certain cities for defeating the monster.</li>
    <li>Trade guild -- as mentioned, this is where the high-priced goods are sold and purchased. Each guild has a rotating list of three items that it buys and three that it sells. There is no need to "find tradegood for sale" as in G3. If a good is not bought/sold for several months, it's price will decrease/increase each month and it will eventually disappear from the market to be replaced with another. A good trader will identify items in multiple cities and nurture these items with the Trade skills to maximize profit.</li>
    <li>"Conceal Identity of Trader" is now a stealth skill, so traders who want to keep their identity hidden will need to learn that separate category skill.</li>
    </ul>
  </ul>
</ul>
Being a merchant in TAG can be a very challenging career, as you will want at least a couple of highly skilled traders, as well as armed escorts to guard the loads as they are transported through the wilderness.



<p/>
<h3>Other </h3>
<p/>
<p>You MUST specify the ID you wish to use when forming a new noble.
</p>
<p>Quest is gone.
</p>
<p>There is a use for Spiny Root, and Petrus Bones.
</p>
<p>The chance of sucess for Pursuade Oathbound Noble has been increased
from 2% to 10%
</p>
<p>There is no Pledge command. MOST of the functions once covered by
Pleadge are now
covered by using ADMIT. However only the castle holder gets the
garrison reports.
</p>
<p>The PROMOTE command behaves diffrently. As well as promoting the
specified character to the top of the stack
it will also UNSTACK the character that issued the command. If you use
the promote order you must re-issue the
stack order to reform the stack.
</p>
<p>I would like to include a statement from Graeme Anderson (who played
both games)
I have to agree completely with this statement but play both because I
have not
reached that pinnacle of power in Oly G2:<br>
<blockquote> I think TAG really
encourages teamwork between factions and it is slower to get going or
to build up a 'powerful' or wide skill based faction.<br>
On the other hand it has the potential for a longer lasting game since
nobles can be permanently killed and wandering monsters regenerate
themselves eventually and hold a much more intersting variety of
magical items. This aspect of Oly2 I find quite boring: OK I have all the skills
and land I want, what now? Nothing changes unless you are at war ;)
</blockquote></p>
<p>If you come across other major differences, please post in the forum or
email the TAG Moderator.</p>
