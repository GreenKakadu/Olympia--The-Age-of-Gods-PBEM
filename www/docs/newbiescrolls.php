<h3 style="color: rgb(0, 0, 0);">
<a name="ch1"></a><font face="Arial,Helvetica">1) Introduction</font></h3>
<p/>
<h4 style="color: rgb(0, 0, 0);">
<font face="Arial,Helvetica">1.1) Expectations and Goals</font></h4>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Since The
Neverending Kingdom is an open-ended
game, you are able to set your own goals.&nbsp; These could include,
but
are certainly not limited to:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Exploration of the world (in
turn#95,
there
was still a significant chunk of the world that had not yet been
explored)</font></li>
  <li> <font face="Arial,Helvetica">Controlling land:&nbsp; setting up
your own
little duchy, or teaming with other players to control a vast empire</font></li>
  <li> <font face="Arial,Helvetica">Becoming a high priest of one of
the
eight
religions</font></li>
  <li> <font face="Arial,Helvetica">Developing a powerful arch-mage</font></li>
  <li> <font face="Arial,Helvetica">Finding and running trade routes
between cities:
becoming a wealthy merchant</font></li>
  <li> <font face="Arial,Helvetica">Dealing in information and shady
business:
controlling a cadre of spies and assassins</font></li>
  <li> <font face="Arial,Helvetica">Controlling mercenaries: selling
your
services
to other factions and alliances</font></li>
  <li> <font face="Arial,Helvetica">Sailing the high seas: using your
navy
to
control the shipping lanes, or assist traders and other travelers (for
a fee, of course!)</font></li>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">and any
number of other things!&nbsp; Be careful
not to spread your faction too thin at first, however.&nbsp; Pick a
couple
of things you can do really well (and that will make money to support
your
faction), and then spread out as you gain more nobles in your faction.</font>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">1.2)
Starting locations</font></h4>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">There are no
GM-enforced "safe havens" in
The Neverending Kingdom, as in earlier versions of Olympia.&nbsp; Your
nobles are fair game no matter where they start. Fortunately, as of
turn#95,
most places are peaceful, and some alliances or factions are protective
of new players on general principle.&nbsp; You have three options of
how
to choose your starting location:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica"><b>Greyfell [an1b]</b> -- Greyfell
is
in the
"Neuehaven" safe starting zone.&nbsp; All basic skills and some others
are available for teaching.&nbsp; This in itself is a huge
benefit!&nbsp;
Greyfell is also on the edge of the vast wilderness known as the
Wildlands,
as well as being near a long coastline, so that makes it very nice for
a starting location.&nbsp; Check the last few issues of The
Gods Speak for more information.</font></li>
  <li> <font face="Arial,Helvetica"><b>Imperial City [aa0b] (default)</b>
-- you
will start out here if you don't specify anyplace on your signup.&nbsp;
It used to be a very busy city, but all of the land around it has been
claimed and worked over.&nbsp; You may want to consider Greyfell [an1b]
as a better alternative.&nbsp; Imperial City teaches Combat [1100], and
there are guilds for most skill schools located in the city (you will
need
an escort to enter them at first).</font></li>
  <li> <font face="Arial,Helvetica"><b>A starting city specified by
the
city ID
number.&nbsp;</b> You will need to check with the current
players to find a good starting city by this method.&nbsp; If you are
interested in exploration, you should probably choose this method to
find
a city on the edge of the explored territories.&nbsp; Otherwise, try to
find a city that is home to a welcoming faction who is willing to teach
your nobles some of the basic skills or escort them into the guildhalls
where they can learn on their own.</font></li>
  <li> <font face="Arial,Helvetica"><b>A random city, controlled by
some
faction.</b>&nbsp;
This will start your first noble in a totally random city that could be
controlled by anyone.&nbsp; The city must be attached to a castle (via
a garrison)
in order to start there, so you may be an unwelcome guest before you
are
even able to establish yourself.&nbsp; It will not let you start in a
city
that has not yet been explored and garrisoned.&nbsp; Additionally, you
might end up in a city with no other occupants (except for the garrison
outside), and that doesn't teach any skills (most cities don't)!&nbsp;
A bad situation indeed.&nbsp; Unless you are really adventurous, you
should
probably go with one of the first two options.</font></li>
</ul>
<p/>
<h3 style="color: rgb(0, 0, 0);">
<a name="ch2"></a><font face="Arial,Helvetica">2) Your nobles</font></h3>
<p/>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">The primary
vehicles through which you achieve
your goals are your nobles (the characters your control in the
game).&nbsp;
You start off with one noble, and 12 "noble points" (NPs).&nbsp; NPs
can
be used to form (or "buy") new nobles, to increase the loyalty of
existing
nobles (see "loyalty",
or to learn special advanced skills (religion, magic, and heroism).</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Nobles are
like the featured actors in
a movie -- they are the ones we are really interested in, the heroes
and
heroines of Olympia.&nbsp; They can hire men, control beasts, and carry
out complex orders.&nbsp; These other people or creatures are more like
the "extras" in a movie.&nbsp; You cannot issue orders to them, but you
instead control them through the whims of your noble characters.</font>
</p>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Your first
turn, you will want to form
3-4 new nobles at a cost of 1 NP each.&nbsp; Save some of your NPs for
later -- you will be glad that you did.&nbsp; You receive 1 new NP
every
8 turns.&nbsp; In TAG, you do not receive any NP back if a noble is
killed
(unlike Oly 2).</font>
</p>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">2.1)
Nobles: Loyalty</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Nobles are
not automatically guaranteed to
be yours forever.&nbsp; They may have their own motivations to leave
your
service, unless you pay them well, make them afraid to leave, or oath
them
to your faction permanently.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">You will
only want to use "oath"
loyalty on your most important nobles, since it will cost you 1 or 2
NP.&nbsp;
Fear or contract loyalty is appropriate for most resource-gatherer-type
nobles.&nbsp; These types of loyalty are enforced with the honor
and terrorize
commands.&nbsp; Here is a comparison of these two loyalty types:</font>
</p>
<p style="color: rgb(0, 0, 0);"><b><font face="Arial,Helvetica">Contract:</font></b>
</p>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">pros:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">your noble cannot be bribed with
a
paltry
250 gold</font></li>
    <li> <font face="Arial,Helvetica">the noble can still terrorize
other
nobles</font></li>
    <li> <font face="Arial,Helvetica">no chance of illness due to
being
terrorized</font></li>
  </ul>
  <li> <font face="Arial,Helvetica">cons:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">costs 50 gold/noble/month to
maintain</font></li>
    <li> <font face="Arial,Helvetica">if someone really wants to bribe
the noble, they usually can offer more than the contract has honored
them</font></li>
  </ul>
</ul>
<b style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Fear:</font></b>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">pros:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">no cost to maintain</font></li>
    <li> <font face="Arial,Helvetica">if terrorized
by a priest they are dedicated to, they can be healed immediately,
stopping
infections and allowing many months of service before having to be
terrorized
again</font></li>
  </ul>
  <li> <font face="Arial,Helvetica">cons:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">if they don't have a healing
potion
and aren't
with a priest when terrorized, then there is a chance they can catch an
infection.&nbsp; Once they have an infection, they can die fairly
easily,
even in an inn.</font></li>
    <li> <font face="Arial,Helvetica">can be bribed with a paltry 250
gold</font></li>
    <li> <font face="Arial,Helvetica">For G2 players: Survive
Fatal Wound is now a Heroism
guild skill, and therefore costs 1 NP to learn.&nbsp;&nbsp; You cannot
use the "infinite terror" tactic from G2 to kill a noble and have him
recover
from the wound with "fear-100" loyalty (except Heroes, of course;
thanks
to Corum of Tanelorn for the tip)</font></li>
    <li> <font face="Arial,Helvetica">there is something of a moral
issue
here :-)</font></li>
  </ul>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">When you
start out, you won't need to worry
about this for about 10 turns, but keep it in mind.&nbsp; You don't
want
to have to oath all of your nobles right away if you are running low on
your starting cash, so another possibility is to use low levels of
terror
(say 5-10%) and make sure the noble is inside an inn to reduce the
chances
of infection.</font>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">2.2)
Nobles: Noble Points</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">As mentioned
before, Noble Points can be used
to create nobles and to learn special skills.&nbsp; If you want to
learn
very many special skills, you need to save your NPs, since you only get
one new one every 8 turns.&nbsp; NP costs for different noble types are:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">resource gatherer
(wood/mining/horses/etc):
1 NP</font></li>
  <li> <font face="Arial,Helvetica">combat leader versus monsters: 1 NP</font></li>
  <li> <font face="Arial,Helvetica">combat leader versus other
players: 2
NP (oath-1;
3 NP if oath-2)</font></li>
  <li> <font face="Arial,Helvetica">castle sitter: 2-3 NP (oath-1 or
preferably
oath-2)</font></li>
  <li> <font face="Arial,Helvetica">Hero: 2 NP (3 if oath-1)</font></li>
  <li> <font face="Arial,Helvetica">Priest: 3-4 NP (oath-1; 4 NP if
requires "create
holy symbol")</font></li>
  <li> <font face="Arial,Helvetica">expendable minor mage: 2 NP (knows
1
skill,
like Gatecraft, with a narrow focus)</font></li>
  <li> <font face="Arial,Helvetica">non-expendable minor mage: 5 NP
(oath-1; necromancy;
transcend
death; one other skill;&nbsp; 6 NP for a 3rd magic skill)</font></li>
  <li> <font face="Arial,Helvetica">master magician: 10 NP (oath-1; 5
magic schools;
2-NP "penalty" for exceeding 3 skills; transcend death)</font></li>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">If you are up
against a really clever opponent,
you may want to oath-2 your castle-sitter and combat leaders, so they
won't
fall victim to "persuade
oathbound noble" (note that this has a 10% chance of success each week
it is used, rather than a 2% chance as in earlier Olympias).&nbsp;
Remember
that in TNK the bribing faction has to pay the NP cost of any nobles
they
bribe, so most of your high-NP nobles should be safe from bribery and
persuasion
attempts unless your enemy has stored up a large amount of NPs (not
likely,
but possible).</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Noble
Points are your most precious resource,
so be sure to protect them!&nbsp; All non-mage nobles should be
dedicated
to a priest before doing anything too hazardous.&nbsp; This will allow
the noble to be resurrected in case</font>
<br>
<font face="Arial,Helvetica">of death.&nbsp;
Remember, though, that you only have 12 turns to get a dead body to the
priest in a temple, or your noble will be lost forever.&nbsp; Mages
should
learn the Necromancy
subskill Transcend
Death to keep from permanently dying, since they cannot be dedicated
to a priest.</font>
</p>
<p/>
<h3 style="color: rgb(0, 0, 0);"><a name="ch3"></a><font
 face="Arial,Helvetica">3) Some first steps</font></h3>
<p/>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">So, you have
formed some nobles, now what
do you do?</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">One of the
first things you need to do is to establish some sources of
income.&nbsp; Don't squander that first 5000 gold, but use it to learn
the skills that you will need to survive.&nbsp; There is a tips page
for making money and another one for some general advice that you
should read.</font> </p>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">3.1)
Skills</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">You should
learn some, but not necessarily
all, of the basic skills.&nbsp; You will probably start with enough
Study
Points to learn 2-3 of the basic skill categories.&nbsp; These allow
you a certain number of days of study that will not require a
teacher.&nbsp;
(WARNING:&nbsp; Don't have the same noble use them for more than a week
each turn, as the second week of study isn't as productive as the
first!)&nbsp;
Note that magic, religion, alchemy, heroism, persuasion, and stealth
skills
cannot be learned with study points.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Some
cities teach basic skills (for instance,
Imperial City [aa0b] teaches Combat in the city proper).&nbsp; Also, be
sure to ask around to see if anyone can help teach your new
nobles.&nbsp;
Explain that you are a new faction just setting foot in the world, and
most established players will be glad to teach you at least some of the
basic skills.&nbsp; Guilds
also teach skills, but you will have to be escorted inside by a
guildmaster.&nbsp;
Watch to see if anyone enters the guild tower, and they are a likely
candidate
to escort your nobles in, if you ask nicely.&nbsp; Trade guilds are
found
in almost every city, while Shipcraft and Combat guilds are probably
the
next most popular.</font>
</p>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">3.2) What
to learn?</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">So many
skills, so little time (and money!)&nbsp;
You should only learn the most necessary skills to start off with, or
you
will soon go bankrupt.&nbsp; Since study
becomes less effective after the first week you study each month,
consider
only studying 1-2 weeks each month and then doing something else.&nbsp;
Remember that each new skill category or subskill that you learn will
cost
100 gold.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Here is an
overview of the non-Magic, non-Religion
categories and which will be most useful to you starting out:</font>
</p>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Combat:&nbsp;
The most basic survival skill.&nbsp; If you are leaving the city, an
armed
escort is a must.&nbsp; Most useful subskills:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">Instill
Combat Discipline:&nbsp; although skirmishers (3,3,0) may seem more
cost-effective that soldiers (5,5,0) since they cost half as much to
maintain,
they tend to run away in battle.&nbsp; ICD will allow you to train
soldiers
that can be turned into better troops such as pikemen and light
crossbowmen.</font></li>
    <li> <font face="Arial,Helvetica">Weaponsmithing:
allows you to make crossbows and pikes, among other things.&nbsp; A
must
if you want pikemen and lgt xbowmen.</font></li>
  </ul>
</ul>
  <p><br>
  <font face="Arial,Helvetica">You can get by with just these two
skills starting out, assuming that the only military threat is from
monsters
and not from other players.&nbsp; If you are attacking monsters,
though,
you will need to be more prepared:</font></p>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <ul>
    <li> <font face="Arial,Helvetica">Fight
to the Death: this skill will allow you to decide when to quit
fighting.&nbsp;
By default, your side will give up when 50% of it's strength (total
offensive+defensive
values) has been depleted.&nbsp; This skill allows you to manipulate
the
"we give up" percentage to as low as 0%.&nbsp; In this case, your side
will&nbsp;&nbsp;&nbsp;&nbsp; fight until all the soldiers are dead and
all the nobles have suffered a wound.&nbsp; The downside of FttD is
that
your force may be much smaller than the enemy after the battle, which
will
greatly increase your chance of being captured if you lose.&nbsp; Note
that monster stacks always fight to the death.</font></li>
    <li> <font face="Arial,Helvetica">Control
Men
in Battle:&nbsp; allows you to control more men in a fight.&nbsp;
If you attack, you can control (10+CMIB) men.&nbsp; If you are
attacked,
you can control (30+CMIB) men.&nbsp; Needless to say, you want to
practice
CMIB often if you want to command a bigger army.</font></li>
  </ul>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Forestry:
in
order to make basic weapons, build ships, and build inns, you need
wood.&nbsp;
Some cities buy wood.&nbsp; Learn Harvest
Wood.&nbsp; If you are in a city that buys some of the items that
you
can collect with&nbsp; Collect
rare
foliage then you may want to also learn that skill.&nbsp; Ignore
the rest of the subskills for now.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Trading:&nbsp;
training
a trader is expensive (900 gold), but it is a very good way to
make money to support your faction.&nbsp; The skills in themselves are
not very useful for the most part (except for Build
Wagons), but the important thing is access to the trading guilds.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Ranger:&nbsp;
the
horse skills are very useful, and Find
Food
in Wilderness can be used to reduce the cost of maintaining your
exploration party.&nbsp; Riding horses can be used to make wagons (as
well
as to increase your speed), and some cities purchase wild horses.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Construction:
since
you receive a free tower in your "claim", you probably won't need
to build anything early in your faction's life cycle.&nbsp; However,
many
smaller cities purchase stone, and so a noble who knows Stone
quarrying many be a good investment if you are near a mountain or
rocky
hills.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Persuasion:
this
category has a few skills that might be useful to a beginning faction,
if you can find a teacher for it.&nbsp; The "wild men" and "angry mob"
skills give you a free fighting force.&nbsp; "Bribe
noble" can be used to persuade refugees to join you, but it is not
guaranteed to work and is therefore probably risky&nbsp;&nbsp; when you
are low on money.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Shipcraft:&nbsp;
This
is an all-or-nothing proposition.&nbsp; Decide if you want to sail
a ship.&nbsp; If so, learn all of the non-guild skills, then enter (or
create) a guild and learn Navigate
Deep
Seas.&nbsp; Total cost for a guilded sea captain is 700 gold (+250
if you have to create the guild).</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Stealth:
this
probably isn't very useful when you are starting out. Conceal
Self is one of the best skills you can learn, but it will cost you
1000 gold and many months before you can even study it.&nbsp; Save this
category for later.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Alchemy:
though
it is very useful to have a healing potion in the possession of
each of your nobles, it can be an expensive chore to make them.&nbsp;
Save
this skill for later, and dedicate your nobles to a priest.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Mining:
mines
are a money pit.&nbsp; You won't find gold until they are fairly
deep, and this requires a mining guild member and a ton of
workers.&nbsp;
Save this skill until you are making money and want to build things
that
require iron.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Heroism:&nbsp;
a
hero can be a fun character to play, and are potentially very useful
for attacking monsters in locations with small entrances.&nbsp;
However,
they cost a lot of money to develop, so you probably want to save this
skill for later.</font></li>
</ul>
<p/>
<p/>
<h3 style="color: rgb(0, 0, 0);">
<a name="ch4"></a><font face="Arial,Helvetica">4) Movement - getting
around
in the world</font></h3>
<p/>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">There are a
number of ways to move around
from place to place.&nbsp; Some are faster than others.&nbsp; Unless
you
really like to take your time getting from one place to the next, you
will
probably want to spend some time upgrading your movement
capabilities.&nbsp;
Be sure to read the rules on carrying
capacity
and travel.&nbsp; Some ways to get around include:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">walking: this is your basic
movement
type,
with times reported in your turn report.</font></li>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">riding: riding from one province
to
the next
takes half as long as the reported time.&nbsp; Ways to achieve this
include:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">riding horses (carry 150 weight
units each)</font></li>
    <li> <font face="Arial,Helvetica">warmounts (carry 200 weight
units
each)</font></li>
    <li> <font face="Arial,Helvetica">light wagons (carry 500 weight
units
each;
but only move at walking speed in mountains)</font></li>
    <li> <font face="Arial,Helvetica">war wagons (carry 900 weight
units
each; but
only move at walking speed in mountains)</font></li>
    <li> <font face="Arial,Helvetica">heavy wagons (carry 900 weight
units
each)</font></li>
  </ul>
  </ul>
  <p><br>
  <font face="Arial,Helvetica">Warmounts are probably the most
cost-effective
way to move quickly for a new player who plans to go exploring through
unknown terrain (and later you can use them to make knights and
paladins,
so they serve a dual purpose).&nbsp; If you are going for cheap and
easy,
then light wagons are the most efficient if you can tolerate the slow
movement
in the mountains.&nbsp; In the long run, you will probably want to
build
some heavy wagons (requires iron) and only use the light ones for short
hauling duties.</font></p>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">sailing
and
ships: this is a relatively quick way to move around, and is a
bit faster than riding.&nbsp; If you build a ship, be sure to invest in
3 sails for each hull to achieve maximum speed.&nbsp; I'm not convinced
that there is a reason to ever build a ship with rowing ports.&nbsp;
Typical
sailing times between provinces for a fully equipped vessel are:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">ocean to ocean: 4 days</font></li>
    <li> <font face="Arial,Helvetica">ocean to/from land: 1 day</font></li>
    <li> <font face="Arial,Helvetica">ocean to sea lane: 3 days (a sea
lane is a
well-known ocean path that allows speedy travel, and is marked as a
5-day
ocean route rather than 6 days in your turn report)</font></li>
  </ul>
</ul>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">flying:
flying
is a preferred way to travel across land and short ocean routes.&nbsp;
Travel times between provinces are as follows:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">land to land: 3 days</font></li>
    <li> <font face="Arial,Helvetica">ocean to/from land: 2 days</font></li>
    <li> <font face="Arial,Helvetica">to/from 1-day inner location: 1
day</font></li>
  </ul>
  </ul>
  <p><br>
  <font face="Arial,Helvetica">WARNING: if your nobles spend more
than 15 days flying over water, they will fall in and drown!&nbsp;
Never
use flying to explore new territory for this reason.</font> </p>
  <p><font face="Arial,Helvetica">Some beasts are able to carry nobles
while
flying.&nbsp; The easiest to obtain are probably winged horses, which
are
often held captive by monster stacks in the wilderness and can be
purchased
in some mystical cities in other dimensions.&nbsp; Other flying beasts
are much more difficult to obtain.&nbsp; Some Magical artifacts and
spells
exist that allow nobles to fly, and some of the more powerful priests
are
granted angels by their god as a reward for having many followers.</font></p>
</ul>
<p/>
<h3 style="color: rgb(0, 0, 0);">
<a name="ch5"></a><font face="Arial,Helvetica">5) Basic Combat</font></h3>
<p/>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">It is a very
good idea to understand the combat
system before getting into too many fights.&nbsp; More than one
noble
has been lost due to misunderstandings about the way battles
are
resolved.&nbsp; Since the rules provide a good step-by-step
explanation
on the way combat works, we'll just provide some tips here.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">When you
first venture out into the wilderness,
you should train a defensive force for protection of your nobles.&nbsp;
These could be skirmishers or (preferably) soldiers.&nbsp; Try to take
a stack of at least 20, and you <i><u>should</u></i> be safe from most
monster attacks.&nbsp; Make sure that each front-line noble has some
troops.&nbsp;
Most monsters tend to leave larger stacks alone, but there are some
exceptions.&nbsp;
Talk to other nobles in the area and find out what the risks are in
your
starting area.&nbsp; Then, make a line for the nearest forest to gather
some wood and make some weapons for your troops.</font>
</p>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">5.1)
Armies</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">If you want
to go monster-hunting in the wilderness,
then you should build an army that is big enough to take minimal losses
against weaker monster stacks.&nbsp; Be sure to have a few crossbowmen
(or even archers) in the back row.&nbsp; One simple force might be:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">One noble with "Fight
to
the Death" and CMIB=10,
controlling
20 pikemen, behind
0</font></li>
  <li> <font face="Arial,Helvetica">A second noble, with 10 light
xbowmen,
behind
9</font></li>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">In this
example, the crossbowmen provide the
offensive punch (1,1,15) while the pikemen (5,15,0) provide protection
to them.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Try to use
soldiers (5,5) as the minimum
standard for your combat troops.&nbsp; Skirmishers (3,3) aren't
trustworthy
and tend
to run
away when some of their comrades are killed.&nbsp; Always try to
keep
some form of missile troops in your army.&nbsp; Since the missile round
occurs before the melee round during a battle, the missile fire is like
having a free round of attack before the enemy can even react (that is,
unless they have their own missile troops).&nbsp; In addition, if you
are
attacked, you get a free round of missile fire before the battle even
starts!</font>
</p>
<p/>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">5.2)
Battle notes</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Things to
remember:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">The defender may be entitled to
some
defense
bonuses depending on the terrain (see combat
bonuses in the rules).</font></li>
  <li> <font face="Arial,Helvetica">The weather can ruin even the
best-planned
attack if it is windy or raining (see Limits
to
Missile Troops in the rules).</font></li>
  <li> <font face="Arial,Helvetica">Each missile troop in the back
line
must be
protected by someone in the front line in order to attack.&nbsp;
Therefore
it is probably best to have more front-line men (since they are the
ones
getting killed first) than missile troops.</font></li>
  <li> <font face="Arial,Helvetica">Each noble can control 10 men when
their stack
is the aggressor and 30 when the enemy initiated the attack.&nbsp; This
can be increased by knowledge of Control
Men
In Battle.</font></li>
  <li> <font face="Arial,Helvetica">If one side badly outnumbers the
other, all
attackers may not be used.&nbsp; Only 4 times the number of remaining
defenders
are allowed to attack, and these are chosen randomly (so the attacker's
strongest nobles may not get to attack at all in this case!)</font></li>
  <li> <font face="Arial,Helvetica">The stack leader will always be
the
last one
hit.&nbsp; Even monsters have a stack leader!</font></li>
  <li> <font face="Arial,Helvetica">Other nobles and monsters may have
an
artifact
or skill that can improve their combat ratings.</font></li>
</ul>
<p/>
<h4 style="color: rgb(0, 0, 0);">
<font face="Arial,Helvetica">5.3) Battle simulation</font></h4>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">There is an
easy (but pretty crude) way to
simulate a simple battle on paper or in your head.&nbsp; This is good
for
fighting monsters, mostly, since they usually only have one type of
monster
in the stack.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">1) Make a
list of all the men or beasts
involved for each side.&nbsp; For instance:</font>
<br>
&nbsp;
</p>
<center style="color: rgb(0, 0, 0);">
<table bgcolor=black border="1">
  <tbody>
    <tr>
      <td><b>Us:</b></td>
      <td><b>Them:</b></td>
    </tr>
    <tr>
      <td>1 noble (front): (80,80,0)</td>
      <td>20 orcs (20,15,0)</td>
    </tr>
    <tr>
      <td>20 soldiers (front): (5,5,0) <br>
(will fight at 5,10 in the mountains)</td>
      <td><br>
      </td>
    </tr>
    <tr>
      <td>1 noble (back): (80,80,0)</td>
      <td><br>
      </td>
    </tr>
    <tr>
      <td>10 lgt xbowmen (back): (1,1,15)</td>
      <td><br>
      </td>
    </tr>
  </tbody>
</table>
</center>
<p/>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">2) Modify
the combat ratings for any combat
bonuses.&nbsp; In our example, let's pretend that this combat takes
place
in the mountains and that the orcs are attacking our party.&nbsp; In
this
case, we will double our defense bonus so that soldiers = (5,10,0).</font>
</p>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">3) Now, go
through the steps of the battle,
and use the attack/defense ratings to estimate the number of "kills" on
each side.&nbsp; For each attack, we will expect (on average) a number
of defenders to be killed.&nbsp; This number is equal to {(attack
factor)/[(attacker
factor) + (defense factor)} * (number of attackers).&nbsp; For instance:</font>
</p>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Round #0: defender's free missile
phase:&nbsp;
xbowmen attacks at 15 while the orcs defend at 15.</font></li>
</ul>
<center style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">[15/(15+15)]
*
10
(lgt xbowmen) =
0.5 * 10 = 5 orcs killed</font></center>
<blockquote style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">The
first
15
is the attack factor
of the force that is currently attacking (in this case, your missile
troops);
the second 15 is the same; and the third 15 is the orcs' defense factor
(they don't receive the mountain defense bonus since they initiated the
attack.&nbsp; <b>Average Losses:&nbsp; Them: 5 orcs, Us: none</b>
(this
phase only happens before the first round)</font>
  <p><font face="Arial,Helvetica">Note that our back-row noble isn't
rated
for missile attacks, and therefore doesn't join in the attack.</font></p>
</blockquote>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Iterate the
following until one side is gone:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Round#1 - defender missile phase:
same
as
before <b>Average Losses:&nbsp; Them: 5 orcs</b> (but not removed
until
the end of the missile round, since combat is simultaneous)</font></li>
  <li> <font face="Arial,Helvetica">Round#1 - attacker's missile
phase:&nbsp;
orcs aren't rated for missile attacks, so skip this one.</font></li>
  <li> <font face="Arial,Helvetica">Remove all troops lost in the
missile
phase
by both sides now (them: 5 orcs)</font></li>
  <li> <font face="Arial,Helvetica">Round#1 - defender's melee phase:
our
20 soldiers
attack the remaining 10 orcs:</font></li>
</ul>
<center style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">[5/(5+15)]
*
20
(soldiers) = 0.25
* 20 = 5 orcs killed</font></center>
<blockquote style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Also
consider
that
our front-row
noble at (80,80) has an 80/(80+15) = 0.84 = 84% chance to kill an orc
with
his attack, so we can count this as a 6th kill</font></blockquote>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Round#1 - attacker's melee phase:
10
orcs
attack our 20 soldiers (our front line):</font></li>
</ul>
<center style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">[20/(20+10)]
*
10
orcs = 0.67 * 10
= 7 soldiers killed</font></center>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Remove all troops lost in the
melee
phase:</font></li>
  <ul>
    <li> <b><font face="Arial,Helvetica">Average losses: <u>Them: 6
orcs</u> <u>Us:
7 soldiers</u></font></b></li>
    <li> <b><font face="Arial,Helvetica">Remaining: <i><u>Them: 4 orcs</u></i>
      <i><u>Us:
13 soldiers, 10 lgt xbowmen, 2 nobles</u></i></font></b></li>
  </ul>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">It looks like
we are going to win this one
handily, so we can stop here.&nbsp; If it was still in doubt, we could
simulate another round by repeating the process staring with round#2 -
defender's missile phase.&nbsp; If it is close, then the battle could
go
either way and is probably too risky to attempt.&nbsp; Be sure to also
simulate what might happen if you got less than the average number of
kills
in the first round, or if the enemy did better than expected with their
attack.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica"><b>This is
not a foolproof way to determine
combat winners, as there is a lot of randomness in the process!</b>&nbsp;
It
is
merely
a simulation of what, on average, the results should be for
a simple combat situation.&nbsp; You will find out very quickly if you
don't stand a chance.</font>
</p>
<p/>
<h3 style="color: rgb(0, 0, 0);"><a name="ch6"></a><font
 face="Arial,Helvetica">6) The NK World</font></h3>
<p/>
<h4 style="color: rgb(0, 0, 0);">
<font face="Arial,Helvetica">6.1) Cities</font></h4>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Cities are
usually fairly safe, assuming that
the ruling faction isn't paranoid.&nbsp; Monsters have occasionally
been
seen wandering into cities, but it is a rare occurrence.&nbsp; You can
purchase and sell things on the city market, and a few minor trade
items
can be bought and sold there as well.&nbsp; A few useful items you can
buy from cities include:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">riding horses, to move at riding
speed
(double
walking speed).&nbsp; Don't waste your money in the beginning, though,
as you can make these with the Ranger skills.</font></li>
  <li> <font face="Arial,Helvetica">oxen, to help you carry heavy
loads.&nbsp;
Oxen can be used to make hides.</font></li>
  <li> <font face="Arial,Helvetica">iron, in very limited quantities</font></li>
  <li> <font face="Arial,Helvetica">old books and instruction
manuals.&nbsp; You
should only buy these when you are starting out if you know what the
book
teaches.&nbsp; Otherwise, you might be throwing away your money (who
needs
a Manual of Instill Combat Discipline?)</font></li>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">In addition,
some cities buy things (see Making
Money
in TAG).</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">There are
usually towers and guilds of
all sorts inside cities, and many nobles can be found congregating in
some
of the major cities.</font>
</p>
<h4 style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">6.2) The
Wilderness</font></h4>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">The
wilderness is a very dangerous place,
where wandering monsters abound!&nbsp; Surprisingly, many monsters
exhibit
intelligent behaviors.&nbsp; They can pillage populated provinces,
enter
and leave inner locations, and gauge the strength of your party and
whether
or not they want to attack you.&nbsp; For this reason, it is very
important
to take a small army with you when venturing from the city.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">If you are
attacked and lose, there are
usually a few things that can happen, depending on the monster:</font>
</p>
<ol style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">you could be eaten;</font></li>
  <li> <font face="Arial,Helvetica">you could be killed outright by
very
evil
monsters;</font></li>
  <li> <font face="Arial,Helvetica">you could be held prisoner (though
you
are
more likely to be eaten);</font></li>
  <li> <font face="Arial,Helvetica">you could be robbed and then set
free
to fend
for yourself in the wilderness.</font></li>
</ol>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Provinces
with garrisons are less likely to
have wandering monsters, but don't let this make you feel too
safe.&nbsp;
Nobles have been attacked inside garrisoned provinces before, while the
garrison soldiers stood and watched.</font>
<p/>
<h3 style="color: rgb(0, 0, 0);"><a name="ch7"></a><font
 face="Arial,Helvetica">7) Religions</font></h3>
<p/>
<p/>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">One useful
subset of skills in TAG are the
eight
religions.&nbsp;&nbsp; But which ones should you spend your time
and
NP learning?&nbsp; Should you bother learning any?&nbsp; There are
several
factors that might influence your decision.</font>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">One of the
most useful things a priest
can do is to resurrect
your nobles who get killed, and heal
those who are injured in battle or perhaps through terror.&nbsp; In
order
to do this, the noble to be helped must be a follower
of the priest who is helping them.&nbsp; For these basic functions, any
religion will do.&nbsp; In fact, if you aren't interested in learning
any
of the religious skills, you could just dedicate your nobles to a
friendly
priest from another faction who you know will be available in case of
an
emergency.</font>
</p>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">Unlike the
magical arts, the religion skills
are very competetive in nature.&nbsp; The three priests who have the
most
followers in each religion are very powerful and are awarded
the
most piety (similar to a mage's aura) and the protection of angels
by their god.&nbsp; The remaining priests may or may not be awarded any
piety based on the number of followers they each have.&nbsp; These
priests
must spend a lot more time using Devotional
Prayer to increase their piety up to useful levels than the top
three
priests do.</font>
</p>
<p style="color: rgb(0, 0, 0);"><font face="Arial,Helvetica">When
choosing a religion, you should think
about a couple of things:</font>
</p>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">do you just want to have a priest
to
dedicate
your nobles to (for resurrection/healing purposes?&nbsp; If so, then
one
of the less-popular religions may be a good choice, since there is less
competition for the piety awarded.</font></li>
  <li> <font face="Arial,Helvetica">If you are intereted in a popular
religion,
don't expect any free gifts of piety until you get a large number of
followers.&nbsp;
On the other hand, you shouldn't let this bother you if you are truly
interested
in a particular religion.&nbsp; Just be sure to save enough piety to
resurrect
a dead noble in case of an emergency.</font></li>
</ul>
<font style="color: rgb(0, 0, 0);" face="Arial,Helvetica">Here is a
brief reiew of each religion:</font>
<ul style="color: rgb(0, 0, 0);">
  <li> <font face="Arial,Helvetica">Church
of
Eres [2000] - probably the most useful religion for combat; weather
control is a powerful tool; may only be useful if you are actually at
war,
though.</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Anteus [2100] - many useful mining skills; Weaken
Building is a very powerful prayer.</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Dol [2200] - capture
beasts and breed
beasts can give you free troops; powerful monsters in inner
locations
can be very difficult to catch; only combat guild members can use
beasts
in battle, however (and note that Dol priests cannot join the combat
guild!)</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Timeid [2300] - free powerful troops (elves), but they are somewhat
flakey and tend to wander off each month.</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Ham [2400] - receive
vision is a very useful prayer, worth the NP cost of a Ham
priest.&nbsp;
The others are only useful inside large cities with lots of nobles.</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Kireus [2500] - the ability to create
mithril may make this religion very useful to someone who has a
mage
who knows Artifact
Construction.</font></li>
  <li> <font face="Arial,Helvetica">Church
of
Halon [2600] - free troops (fanatics); double taxes is a useful
skill.</font></li>
<li>  <font face="Arial,Helvetica">Church of
Domingo [2700] - free hidden troops (ninjas), but note that a
Domingo
priest cannot learn Control Men in Battle; Mists
of
concealment can hide an entire army.</font>
</li>
</ul>
<p/>
<h3 style="color: rgb(0, 0, 0);">
<a name="ch8"></a><font face="Arial,Helvetica">8) Miscellaneous stuff</font></h3>
<p/>
<ul style="color: rgb(0, 0, 0);">
  <li> <b><font face="Arial,Helvetica">Try to make sure your nobles
are
doing
something productive just about every day.</font></b><font
 face="Arial,Helvetica">&nbsp;
There are several no-cost or lost-cost things they can do to kill time:</font></li>
  <ul>
    <li> <font face="Arial,Helvetica">make
baskets [94] or clay pots [95] to sell on the city market</font></li>
    <li> <font face="Arial,Helvetica">explore
to find items or hidden locations</font></li>
    <li> <font face="Arial,Helvetica">seek
to find hidden nobles</font></li>
    <li> <font face="Arial,Helvetica">repair
an inn or tower</font></li>
    <li> <font face="Arial,Helvetica">recruit
peasants or train
workers/soldiers/etc.</font></li>
    <li> <font face="Arial,Helvetica">sell their services to other
factions as a teacher
for a week</font></li>
  </ul>
  <li> <b><font face="Arial,Helvetica">Things that can ruin your turn:</font></b></li>
  <ul>
    <li> <b><i><font face="Arial,Helvetica">Improper use of the wait
command.</font></i></b><font face="Arial,Helvetica">&nbsp; Read the
rules
on this carefully.&nbsp; Then, read them again!&nbsp; In fact, read
them
each time you use the command until you have this section of the rules
memorized!&nbsp; Note that multiple events can trigger a wait.&nbsp;
For
instance "wait teacher 1000 day 10" will wait for someone to teach
shipcraft
[1000] or day 10, whichever comes first.&nbsp; This keeps you from
relying
too much on other nobles, who may mess up their orders.</font></li>
    <li> <b><i><font face="Arial,Helvetica">Forgetting to set your
accepts
and admits
properly
(both you, and those whom you are interacting with).</font></i></b><font
